# REGRESSION ANALYSIS
# TRAINING A MODEL 
# FIT=TRAIN



# formular
# y = B1 + B2 * x + e

# y is dependent
# B1 is intercept
# B2 is slope
# x is independent *e is error
# linear regression is under supervised machine learning

# The primary objective of the linear regression is to achieve the best fitting line between the predicted and the actual values

import numpy as np
import matplotlib.pyplot as plt
from sklearn.linear_model import LinearRegression
np.random.seed(2)
x = 2 * np.random.rand(100, 1)
y = 4 + 3 * x + np.random.rand(100, 1)                   #read up on noise

model = LinearRegression()  #instantiate model
model.fit(x,y) #fit model to the data (note 'x' is independent & 'y' is dependent) make prediction using train model 
x_new = np.array([[0], [1]])  #creating new data point for the predictations
y_pred = model.predict(x_new) 

#plot the datapoint and the linear regression line
plt.scatter(x,y, label = 'datapoint')
plt.plot(x_new, y_pred, 'r-', label = 'LinearRegression')
plt.xlabel('x')
plt.ylabel('y')
plt.title('LinearRegression')
plt.legend()
plt.show()

